<b><u>Background</b></u><br>
Intrusion Detection System in Hospitality Industry is an enhanced Intrusion Detection System to detect the alarm in the hotel rooms that will be implemented in every 1 to 3 star hotels. There is no major security enhancements in hotel rooms due to the intruders that are often stealing valuable belongings in the hotel room. Logging is monitored using PIR sensor. The camera will capture the intruder using photo capture and video capture simultaneously with PIR Sensor.<br><br/>

<b><u>Aim of the Prototype</b></u><br>
Hospitality Intursion Detection System is to  detect the alarm in the hotel room when guests are stepping out of hotel rooms during the day. During the day, an unseen inturder comes into the room who steals valuable belongings from the hotel room as PIR sensor will trigger the alarm with the camera and record the logging time of the detection of the intruder. Then,  SMS will be sending text messages to hotel guests for notification of the movement of intruder. Daily reminders will send SMS for hotel guests to be more aware of valuable belongings. <br><br/>

